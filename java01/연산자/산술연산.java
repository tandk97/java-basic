package 연산자;

public class 산술연산 {

	public static void main(String[] args) {
		// int
		int x = 100;
		int y = 200;
		System.out.println("더한 값은 " + (x + y));
		System.out.println("뺀 값은 " + (x - y));
		System.out.println("곱한 값은 " + (x * y));
		System.out.println("나눈 값은 " + (x / y));
		System.out.println("나눈 나머지는 " + (x & y));

	}

}
