package 변수;

public class 기본데이터 {

	public static void main(String[] args) {
		// 정수, 실수, 문자열, 논리
		// RAM에 넣어보자
		int age = 100; // 정수
		double height = 122.2; // 실수
		char gender = '남'; // '' 문자 1개만
		boolean food = true; // 논리 ==> true(1), false(0)
		String name = "홍길동";
		System.out.println("나의 이름은 " + name);
		System.out.println("나의 나이는 " + age);
		System.out.println("나의 키는 " + height);
		System.out.println("나의 성별은 " + gender);
		System.out.println("내가 밥을 먹었는지 여부 " + food);
		
	}

}
